    private void selectData() {
        String kolom[] = {"NAMA","RAWON","DEGAN","HARGA RAWON","HARGA DEGAN","DISCOUNT","TOTAL HARGA","SUB TOTAL"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_transaksi";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()) {
                String Nama = rs.getString(1);
                String Rawon = rs.getString(2);
                String Degan = rs.getString(3);
                String HargaRawon = rs.getString(4);
                String HargaDegan = rs.getString(5);
                String Diskon = rs.getString(6);
                String Totalharga = rs.getString(7);
                String SubTotal = rs.getString(8);
                String data[] = {Nama,Rawon,Degan,HargaRawon,HargaDegan,Diskon,Totalharga,SubTotal};
                dtm.addRow(data);
                            
            }
        } catch (SQLException ex){
            //Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbData.setModel(dtm);
    }
