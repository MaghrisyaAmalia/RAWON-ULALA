    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if ("".equals(nama.getText()) || "".equals(jmlra.getText()) || "".equals(jmlde.getText()) || "".equals(hrgra.getText()) || "".equals(hrgdg.getText()) || "".equals(disk.getText()) || "".equals(totalhar.getText()) || "".equals(sub.getText()))
        {
            JOptionPane.showMessageDialog(this, "Lengkapi Datamu Dulu Yaa", "Kamu ERROR", JOptionPane.WARNING_MESSAGE);
        } else {
                String SQL = "INSERT INTO tb_transaksi (nama,rawon,degan,hargarawon,hargadegan,discount,totalharga,subtotal) " + "VALUES('"+nama.getText()+"','"+jmlra.getText()+"','"+jmlde.getText()+"','"+hrgra.getText()+"','"+hrgdg.getText()+"','"+disk.getText()+"','"+totalhar.getText()+"','"+sub.getText()+"')";
                int status = KoneksiDB.execute(SQL);
                if (status == 1){
                    JOptionPane.showMessageDialog(this, "Data Kamu Berhasil Ditambahkan", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Data Kamu GAGAL Ditambahkan", "GAGAL", JOptionPane.WARNING_MESSAGE);
                }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int baris = tbData.getSelectedRow();
        if (baris != -1) {
            String NAMA = tbData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM tb_transaksi WHERE nama='"+NAMA+"'";
            int status = KoneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data Kamu Berhasil Dihapus", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data Kamu GAGAL Dihapus", "GAGAL", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Kamu Pilih Baris Data Terlebih Dahulu", "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

