    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
       int r = Integer.parseInt(jmlra.getText());
       int d = Integer.parseInt(jmlde.getText());
       int tr = r * 20000;
       int td = d * 5000;
       hrgra.setText(String.valueOf(tr));
       hrgdg.setText(String.valueOf(td));
       int hrawon = Integer.parseInt(hrgra.getText());
       int hdegan = Integer.parseInt(hrgdg.getText());
       int htot = hrawon + hdegan;
       totalhar.setText(String.valueOf(htot));
       if (htot > 200000){
           int diskon = htot * 20 / 100;
           disk.setText(String.valueOf(diskon));
       } else {
           disk.setText(String.valueOf(htot));
       }
       int diss = Integer.parseInt(disk.getText());
       int subtot = htot - diss;
       sub.setText(String.valueOf(subtot));
       
    }//GEN-LAST:event_totalActionPerformed

